class Color:
    def __init__(self, id_color = None, color = None):
        self.id_color = id_color
        self.color = color
    
    def get_id_color(self):
        return self.id_color
    
    def set_id_color(self, id_color):
        self.id_color = id_color
    
    def get_Color(self):
        return self.color
    
    def set_Color(self, color):
        self.color = color
