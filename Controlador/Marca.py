class Marca:
    def __init__(self, id_marca = None, marca = None):
        self.id_marca = id_marca
        self.marca = marca
    
    def get_id_marca(self):
        return self.id_marca
    
    def set_id_marca(self, id_marca):
        self.id_marca = id_marca
    
    def get_Marca(self):
        return self.marca
    
    def set_Marca(self, marca):
        self.marca = marca
    